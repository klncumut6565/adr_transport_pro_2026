CREATE TABLE IF NOT EXISTS audit_logs (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                timestamp TEXT DEFAULT CURRENT_TIMESTAMP,
                user_name TEXT,

CREATE TABLE IF NOT EXISTS shipments (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                document_no TEXT NOT NULL UNIQUE,
                document_date TEXT,
                status TEXT DEFAULT 'Taslak',

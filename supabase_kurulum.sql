CREATE TABLE IF NOT EXISTS packaging_types (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                code TEXT NOT NULL UNIQUE,
                name_tr TEXT,
                name_en TEXT,
